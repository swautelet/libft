#include"libft.h"

void	ft_lstadd_back(t_list **alst, t_list *new)
{
	if (alst)
	{
		if (*alst == NULL)
			*alst = new;
		else
		{
			*alst = ft_lstlast(*(alst));
			(**alst).next = new;
		}
	}
}
